package Operators;

public class Operators {
  public static void main(String[] args) {
    int number_one = 84;
    int number_two = 26;
    int result1 = number_one - number_two / 2;
    int result2 = (number_one - number_two) / 2;

    System.out.println(result1);
    System.out.println(result2);
  }
}
